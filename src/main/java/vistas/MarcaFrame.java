    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        //Creamos el objeto llamado nuevaMarca -> crear un nuevo registro
        Marca nuevaMarca = new Marca(txtNombre.getText());
        
        try{
            marcaDao.insertar(nuevaMarca);
            JOptionPane.showMessageDialog(this, "Marca guardada exitosamente");
            cargar();
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error al guardar");
        }
    }//GEN-LAST:event_btnAgregarMouseClicked

    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        //ID
        this.txtID.setText(
                //getValueAt(filaSeleccionada, posicionColumna)
                tblData.getValueAt(tblData.getSelectedRow(), 0).toString()
        );
        //Nombre
        this.txtNombre.setText(
                //getValueAt(filaSeleccionada, posicionColumna)
                tblData.getValueAt(tblData.getSelectedRow(), 1).toString()
        );
    }//GEN-LAST:event_tblDataMouseClicked

    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        Marca marcaModificada = new Marca(Integer.parseInt(txtID.getText()),txtNombre.getText());
        
        try{
            marcaDao.actualizar(marcaModificada);
            JOptionPane.showMessageDialog(this, "Marca editada exitosamente");
            cargar();
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error al editar");
        }
    }//GEN-LAST:event_btnEditarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        Marca marcaEliminada = new Marca(Integer.parseInt(txtID.getText()));
        
        try{
            marcaDao.eliminar(marcaEliminada);
            JOptionPane.showMessageDialog(this, "Marca eliminada exitosamente");
            cargar();
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

